a = input('Digite algo: ')
print('\033[30;46m O tipo primitivo desse valor é \033[m ', type(a))
print('\033[30;46m Só tem espaços? \033[m',a.isspace())
print('\033[30;46m É um número? \033[m',a.isnumeric())
print('\033[30;46m É alfabético? \033[m',a.isalpha())
print('\033[30;46m É alfanumérico? \033[m',a.isalnum())
print('\033[30;46m Está em maiúsculas? \033[m',a.isupper())
print('\033[30;46m Está em minúsculas? \033[m',a.islower())
print('\033[30;46m Está capitalizada? \033[m',a.istitle())